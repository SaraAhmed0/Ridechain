/*
 
 dgreen >> dark green
 lgreen >> light green
 dblue  >> dark blue
 lblue  >> light blue
 slblue >> super light blue
 
 
 
 
 
 
 you can use them like that:
 
   1-    .foregroundColor(colorp.lblue)
 
   and for backgrounds:
   2-    .background(colorp.lblue)
 
 
 
 */








import Foundation
import SwiftUI

struct colorp{
    static let dgreen = Color("dgreen") //dark green
    static let lgreen = Color("lgreen") //light green
    static let lblue = Color("lblue") //light blue
    static let dblue = Color("dblue") //dark blue
    static let slblue = Color("slblue")// super light blue
   
   
    
    
}
